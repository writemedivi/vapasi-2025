package oops.Association;

public class Ram {


    private int size;
    private int speedInGHZ ;

    Ram(int size , int speedInGHZ){
        this.size = size;
        this.speedInGHZ = speedInGHZ ;
    }


    public int getSize() {
        return size;
    }

    public int getSpeedInGHZ() {
        return speedInGHZ;
    }

}
